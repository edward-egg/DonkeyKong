package pojo;

import java.io.Serializable;
import java.sql.Timestamp;

/**
 *
 * @author Jason
 */
public class HotelComment implements Serializable{
     private String hotelCommentId;
     private Hotel hotel;
     private Users users;
     private String commentContent;
     private Timestamp commentTime;

    public HotelComment() {
    }

    public HotelComment(String hotelCommentId, Hotel hotel, Users users, String commentContent, Timestamp commentTime) {
        this.hotelCommentId = hotelCommentId;
        this.hotel = hotel;
        this.users = users;
        this.commentContent = commentContent;
        this.commentTime = commentTime;
    }


    public String getHotelCommentId() {
        return hotelCommentId;
    }

    public void setHotelCommentId(String hotelCommentId) {
        this.hotelCommentId = hotelCommentId;
    }

    public Hotel getHotel() {
        return hotel;
    }

    public void setHotel(Hotel hotel) {
        this.hotel = hotel;
    }


    public Users getUsers() {
        return users;
    }

    public void setUsers(Users users) {
        this.users = users;
    }

    public String getCommentContent() {
        return commentContent;
    }

    public void setCommentContent(String commentContent) {
        this.commentContent = commentContent;
    }

    public Timestamp getCommentTime() {
        return commentTime;
    }

    public void setCommentTime(Timestamp commentTime) {
        this.commentTime = commentTime;
    }
     
}
